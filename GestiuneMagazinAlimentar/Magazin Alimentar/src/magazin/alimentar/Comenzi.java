    public ArrayList<Comanda> comandaList(){
        ArrayList<Comanda> comandaList = new ArrayList<>();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/magazin_alimentar", "root", "Verzisori123");
            String query2="SELECT * FROM comenzi";
            Statement st = con.createStatement();
            ResultSet rs=st.executeQuery(query2);
            Comanda comanda;
            while(rs.next()){
                comanda =new Comanda(rs.getInt("comanda_id"), rs.getInt("produs_id"), rs.getInt("cantitate"), rs.getInt("angajati_id"), rs.getString("data_comanda"));
                comandaList.add(comanda);
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        return comandaList;
        
    }
    
    public void show_comanda(){
        ArrayList<Comanda> list = comandaList();
        DefaultTableModel model = (DefaultTableModel)tabelcomenzi.getModel();
        Object[] row = new Object[6];
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getcomanda_id();
            row[1]=list.get(i).getprodus_id();
            row[2]=list.get(i).getcantitate();
            row[3]=list.get(i).getangajati_id();
            row[4]=list.get(i).getdata_comanda();           
            model.addRow(row);
        }
    }
       

