    private void comandaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comandaActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/magazin_alimentar", "root", "Verzisori123");
            String query3= "insert into comenzi(comanda_id, produs_id, cantitate, angajati_id, data_comanda)values(?,?,?,?,?)";
            PreparedStatement pst= con.prepareStatement(query3);
            pst.setString(1, id_comanda.getText());
            pst.setString(2, id_produs.getText());
            pst.setString(3, cantitate.getText()); 
            pst.setString(4, id_angajat.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String data_comanda = sdf.format(data.getDate());
            pst.setString(5,data_comanda);
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel)tabelcomenzi.getModel();
            model.setRowCount(0);
            show_comanda();
            JOptionPane.showMessageDialog(null,"Comanda Plasata!");
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
        
    }//GEN-LAST:event_comandaActionPerformed

